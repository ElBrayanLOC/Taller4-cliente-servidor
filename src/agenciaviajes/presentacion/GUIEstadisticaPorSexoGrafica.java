    private void graficar(ArrayList<Cliente> clientes) {
        setVisible(true);
        int h = 0, m = 0;
        for (Cliente cli : clientes) {
            if(cli.getSexo().equals("Masculino")){
                h++;
            }
            if(cli.getSexo().equals("Femenino")){
                m++;
            }
        }
	int promHombres = (h*100)/(h+m);
	int promMujeres = (m*100)/(h+m);
        
	txtPromedioHombres.setText(promHombres+"%");
	txtPromedioMujeres.setText(promMujeres+"%");
        pgbBarraHombres.setValue(300-((300*promMujeres)/100));
        pgbBarraMujeres.setValue(300-((300*promHombres)/100));
}
